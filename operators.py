
#bitwise operator
x1 = 5
y1 = 10 
print(x1 & y1)
print(x1 / y1)
print(~x1)
print(x1 ^ y1)
print(x1 << y1)
print(x1 >> y1)

#identity operators
x = [1,2,3,4]
y = x
z = [1,2,3,4]
print(x is y)
print(x is z)
print(x is not z)
#membership operators
text = "aswitha"
print("aswit" in text)
text = [1,2,3,4,5]
print(9 not in text)